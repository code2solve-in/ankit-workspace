package edu.vt.cs5044;

/**
 * Indicates which direction is activated, for a button, handle, or lever with two directions.
 * 
 * IMPORTANT: Please DO NOT modify this file in any way!
 *
 * @author Prof. Oliva
 * @version Spring 2021
 */
public enum Direction {
    
    /**
     * Indicates the button or lever is activated to open the door.
     */
    OPEN,
    
    /**
     * Indicates the button or lever is activated to close the door.
     */
    CLOSE;

}
